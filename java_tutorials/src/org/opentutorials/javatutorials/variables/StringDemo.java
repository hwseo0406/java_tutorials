package org.opentutorials.javatutorials.variables;

public class StringDemo {

	public static void main(String[] args) {
		String first;
		first = "coding";
		
		String second = "World"; //이렇게 한 줄로도 가능
		
		System.out.println(first + second); //codingWorld
		
		String a, b;     //두 변수를 한번에 선언도 가능
		a = "Alex";
		b = "Bob";
		System.out.println(a+b); 
	}

}

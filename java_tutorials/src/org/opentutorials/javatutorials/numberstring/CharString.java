package org.opentutorials.javatutorials.numberstring;

public class CharString {

	public static void main(String[] args) {
		System.out.println("생");
		System.out.println("생활코딩"); //문자열은 "로 묶어야함 '는 안됨, 문자 하나는 "로 가능
		System.out.println("생활코딩"+"입니다"); //문자열끼리 더하기 가능
		System.out.println("1"+"1"); //출력은 11이 됨 
		
		System.out.println("egoing said \"Welcome programming world!\""); // 큰따옴표 출력은 앞에 \하면 됨
		System.out.println("Welcome, \nJava!"); //\n은 줄바꿈

	}

}

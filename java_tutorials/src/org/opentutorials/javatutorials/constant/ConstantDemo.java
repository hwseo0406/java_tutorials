package org.opentutorials.javatutorials.constant;

public class ConstantDemo {

	public static void main(String[] args) {
		//int a = 2.2;
		float a = 2.2F; //2.2는 double이지만 F로 float라고 명시 가능
		double b = 2.2;
		
		long c = 2147483648L; //정수는 가본 데이터타입인 int로 설정되어있기 때문에 L로 명시해줘야함
		byte d = 100; //이건 기본적으로 int

	}

}
